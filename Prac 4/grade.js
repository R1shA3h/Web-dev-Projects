var marks = 87;
if (marks >= 50 && marks <= 65) {
    console.log("C");
}
else if (marks > 65 && marks <= 80) {
    console.log("B");
}
else if (marks > 80 && marks <= 90) {
    console.log("A");
}
else if (marks > 90 && marks <= 100) {
    console.log("A+");
}
else {
    console.log("Error!");
}